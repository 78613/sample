/*
 ****************************************************************************
 *
 ****************************************************************************
 */
size_t
adts_prime_max( const size_t limit )
{
    size_t num   = 0;
    size_t val   = 0;
    size_t prime = 3;

    if (unlikely(1 >= limit)) {
        /* sanity */
        goto exception;
    }

    if (unlikely(2 >= limit)) {
        val = 2;
        goto exception;
    }

    /* Prime # is divisible only by 1 and self */
    while (prime <= limit) {
        for (num = 2; num <= prime; num++) {
            if (0 == (prime % num)) {
                /* divisible */
                break;
            }
        }

        if (num == prime) {
            /* self */
            val = prime;
        }
        prime++;
    }

exception:
    return val;
} /* adts_prime_max() */



